/**
 * La clase ConsoleColors proporciona constantes para
 * cambiar el color del texto en la consola.
 */
public class ConsoleColors {

    /**
     * Constante que representa el código de escape
     * para restablecer el color del texto a su valor
     * predeterminado.
     */
    public static final String RESET = "\033[0m"; // Text Reset

    /**
     * Constante que representa el código de escape
     * para cambiar el color del texto a verde.
     */
    public static final String GREEN = "\033[0;32m"; // GREEN

}